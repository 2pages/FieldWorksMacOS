namespace SIL.FieldWorks.Common.Controls
{
	/// <summary>
	/// Determines the style of the control.
	/// </summary>
	public enum ButtonStyles
	{
		/// <summary>Flat button, pops up when mouse is over it</summary>
		Popup = 0,
